package needfortest;

public class OzonGood {
    private  String goodName;
    private String price;

    public OzonGood(String goodName, String price) {
        this.goodName = goodName;
        this.price = price;
    }

    public String getGoodName() {
        return goodName;
    }

    public String getPrice() {
        return price;
    }
}
